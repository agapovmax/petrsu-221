# Читаем основание степени (a) и показатель (b) из одной строки
# Пример ввода: "5 5" или "3 13"
# split() разделяет строку по пробелам
# map(int, ...) конвертирует каждую часть в целое число
a, b = map(int, input().split())

# Модуль, по которому берём остаток
# Это нужно, чтобы числа не становились слишком большими
MOD = 987654323

# Переменная для накопления результата
# Начинаем с 1, потому что 1 * что угодно = что угодно
# В процессе будем умножать на нужные степени числа a
result = 1

# Берём остаток от деления a на MOD
# Это защита от случая, когда a > MOD
# Пример: если a = 1000000000, то a станет 12345677
# Свойство модуля: (a^b) mod M = ((a mod M)^b) mod M
a = a % MOD

# Главный цикл: работает пока степень b больше 0
# На каждой итерации мы "съедаем" один бит числа b (справа налево)
# Количество итераций = количество бит в b ≈ log₂(b)
# Для b = 1000000000 это примерно 30 итераций
while b > 0:

    # Проверяем младший бит числа b (самый правый в двоичной записи)
    # b % 2 == 1 означает, что последняя цифра в двоичной записи = 1
    # Пример: 13 = 1101₂, младший бит = 1
    # Пример: 6 = 110₂, младший бит = 0
    # Если число нечётное → младший бит = 1
    # Если число чётное → младший бит = 0
    if b % 2 == 1:
        # Если младший бит = 1, значит текущая степень a нужна в результате
        # Умножаем result на текущее значение a и берём остаток
        # Пример: result = 1, a = 3 → result = (1 * 3) % MOD = 3
        # На следующих итерациях a будет a², a⁴, a⁸ и т.д.
        result = (result * a) % MOD

    # Возводим a в квадрат для следующей итерации
    # Это построение степеней: a¹ → a² → a⁴ → a⁸ → a¹⁶ → ...
    # Пример: a = 3 → a = 9 → a = 81 → a = 6561 → ...
    # Берём остаток, чтобы число не росло бесконечно
    # Свойство: (a²) mod M = ((a mod M)²) mod M
    a = (a * a) % MOD

    # Делим b на 2 (отбрасываем младший бит)
    # Это сдвиг вправо в двоичной записи
    # Пример: 13 (1101₂) → 6 (110₂) → 3 (11₂) → 1 (1₂) → 0
    # Эквивалентно битовому сдвигу: b >>= 1
    #b = b // 2
    b >>= 1

# Выводим финальный результат: a^b mod MOD
# В переменной result уже лежит правильный ответ
print(result)
