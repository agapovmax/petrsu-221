n = int(input())                               # количество чисел
numbers = list(map(int, input().split()))      # считываем n чисел в список


def counting_sort_by_digit(arr, exp):
    """
    Сортировка подсчётом по одному разряду.
    arr — текущий массив чисел
    exp — текущий разряд: 1 = единицы, 10 = десятки, 100 = сотни, ...
    """
    output = [0] * len(arr)  # результирующий массив (временный)
    count = [0] * 10         # счётчик для каждой цифры 0..9

    # Шаг 1: подсчитываем, сколько раз встречается каждая цифра в текущем разряде
    for num in arr:
        index = (num // exp) % 10  # извлекаем цифру текущего разряда
        count[index] += 1

    # Шаг 2: превращаем count в массив накопленных сумм.
    # count[i] теперь хранит количество элементов с цифрой <= i,
    # то есть позицию последнего элемента с цифрой i в выходном массиве.
    for i in range(1, 10):
        count[i] += count[i - 1]

    # Шаг 3: расставляем элементы по правильным позициям.
    # Идём с конца, чтобы сортировка была устойчивой
    # (элементы с одинаковой цифрой сохраняют исходный порядок).
    for i in range(len(arr) - 1, -1, -1):
        index = (arr[i] // exp) % 10      # цифра текущего разряда
        output[count[index] - 1] = arr[i] # ставим элемент на нужное место
        count[index] -= 1                  # сдвигаем указатель позиции влево

    return output


def radix_sort(arr):
    """
    Поразрядная (цифровая) сортировка (Radix Sort, LSD — Least Significant Digit).
    Сортирует от младшего разряда к старшему.
    Сложность: O(d * n), где d — число разрядов в максимальном числе, n — размер массива.
    """
    max_val = max(arr)  # находим максимальное число, чтобы знать количество разрядов

    exp = 1  # начинаем с разряда единиц (10^0 = 1)

    # Пока в максимальном числе ещё есть необработанные разряды
    while max_val // exp > 0:
        arr = counting_sort_by_digit(arr, exp)  # сортируем по текущему разряду
        exp *= 10                                # переходим к следующему разряду

    return arr


numbers = radix_sort(numbers)
